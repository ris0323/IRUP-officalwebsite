          <li>Long-term ecological monitoring provides insight of the benefits for organic/eco-friendly farming practices on biodiversity conservation. Results indicate that organic/eco-farming rice fields support the higher species richness and abundance in various taxonomic groups (e.g., aquatic species). It also represents significant difference on species richness between paddy fields in eco-friendly farming and others in conventional practice.</li>
