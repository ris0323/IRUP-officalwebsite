          <li>Regarding the production landscape management, farmers make use of excess water resources in rice paddy and then develop a transformative way to control rice blast disease, literally, turning a disadvantage to a useful mean. Biological pest control experiment attempts to discover the mechanisms of natural resource management integrating traditional wisdom and culture, particularly, emphasis on controlling Golden Apple Snail (GAS) population which is threating the food production and local incomes. Results show that food waste and several natural fruits can serve as effective baits when setting up traps to control GAS spread during the early stage of crop growing seasons. Farmers also implement other practices that can benefit crop production and biodiversity mutually, such as using Azolla as a green manure in rice cultivation, no herbicide spray, and creation of ecological ponds as biological refuge during non-growing time (i.e., rice fields would be left dried). </li>
