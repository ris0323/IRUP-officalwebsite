      <h5 class="mb-4">Challenges</h5>
      <h6>
        Climate extremes (e.g., draught) and insufficient water resources may be common challenges that agriculture production is facing with globally. Unlike of that, Liyu community is blessed with natural resources, including clean water, stable irrigation supply, and fertile soil. It is located downstream of the Liyu reservoir surrounding by forests, secondary forests, and farmlands, which forms the SEPLS in the so-called “Low Elevation Mountain Ecosystem” of Taiwan (i.e., attitude lower below 800 m).
        <br><br>
        There is less human activity and industrial development, leading to lower probability of interference upon wildlife. Therefore, the Liyu community retains richer biodiversity. However, before 2013, most residents practiced conventional farming that is harmful to both human and environments. Since then, Residents began to realize the interlinkages among biodiversity, responsible production and human health and then have been practiced organic/eco-friendly farming (e.g., reduced use of agrochemicals) to protect farmland ecosystem from ongoing habitat loss and degradation. Besides that, the organic (or eco-growing) rice can not only ensure responsible and sustainable production but also enhance human physical health than conventional farming.
      </h6>

